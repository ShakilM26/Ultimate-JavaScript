
// জাভাস্ক্রিপ্টে Modules হলো বড় একটি কোড ফাইলকে ছোট ছোট অনেকগুলো টুকরো বা ফাইলে ভাগ করার পদ্ধতি। চিন্তা করুন একটি বড় লাইব্রেরির কথা; সব বই এক জায়গায় গাদা করে না রেখে বিষয় অনুযায়ী আলাদা আলমারিতে রাখা হয় যেন প্রয়োজনে সহজে খুঁজে পাওয়া যায়।
// মডিউল ব্যবহার করলে কোড ম্যানেজ করা সহজ হয় এবং একই কোড বারবার না লিখে বিভিন্ন জায়গায় ব্যবহার করা যায়।

// 🔥 ১. মডিউলের মূল ধারণা: Export এবং Import

// 🌼 Export: যখন আপনি একটি ফাইলের কোনো ভেরিয়েবল, ফাংশন বা অবজেক্টকে অন্য ফাইলে ব্যবহারের অনুমতি দিতে চান, তখন export ব্যবহার করা হয়।
// 🌼 Import: অন্য কোনো ফাইল থেকে এক্সপোর্ট করা জিনিস নিজের ফাইলে নিয়ে আসার জন্য import ব্যবহার করা হয়।


// 🔥 ২. এক্সপোর্ট করার দুটি ধরন
// জাভাস্ক্রিপ্টে মূলত দুইভাবে এক্সপোর্ট করা যায়:

// 🌼 ক) Named Export (নাম দিয়ে পাঠানো)
// 🌼 একটি ফাইল থেকে আপনি অনেকগুলো জিনিস আলাদা আলাদা নামে পাঠাতে পারেন।

// file: mathUtils.js
export const pi = 3.1416;
export function add(a, b){
    return a + b;
}

export function subtract(a, b) {
    return a - b;
}


// 🌼 খ) Default Export (এককভাবে পাঠানো)
// একটি ফাইল থেকে কেবল একটি জিনিসই ডিফল্ট হিসেবে পাঠানো যায়। সাধারণত মেইন ক্লাস বা ফাংশনের জন্য এটি ব্যবহৃত হয়।

// file: greeting.js
export default function sayHello(user){
    return `Hello &{user}`;
}


// 🔥 ৩. ইমপোর্ট করার নিয়ম
// এখন দেখি অন্য ফাইলে এগুলো কীভাবে ব্যবহার করবো:

// file: main.js
import {pi, add} from './mathUtils.js'; // Named export ইমপোর্ট করার সময় { } ব্যবহার করতে হয়
// Default export ইমপোর্ট করার সময় নাম ইচ্ছামতো দেয়া যায়, (কোনো { } লাগে না)
import welcomeMessage from '.greeting.js';

console.log(pi)
console.log(add(3,4));
console.log(welcomeMessage('Sarah'));


// 🔥 ৪. কেন মডিউল গুরুত্বপূর্ণ (The "Under the Hood" Reason) ❓

// 1. Encapsulation (আলাদা রাখা): মডিউলের ভেতরের ভেরিয়েবলগুলো বাইরের দুনিয়া থেকে গোপন থাকে যতক্ষণ না আপনি সেগুলোকে export করছেন। 
// এটি নেমিং কলিশন (একই নামের দুটি ভেরিয়েবল মিলে যাওয়া) আটকায়।
// 2. Strict Mode: মডিউল সবসময় স্বয়ংক্রিয়ভাবে "Strict Mode" এ চলে, যা আপনার কোডকে আরও সুরক্ষিত এবং আধুনিক রাখে।
// 3. Dependency Management: বড় প্রজেক্টে কোন ফাইল কোন ফাইলের ওপর নির্ভরশীল, তা মডিউল সিস্টেম খুব স্পষ্টভাবে বুঝিয়ে দেয়।


