// এরা একে অপরের সাথে গভীরভাবে জড়িত, কিন্তু এদের কাজ করার ধরণ কিছুটা আলাদা। সহজভাবে বললে: Lexical Scope হলো একটি "নিয়ম", 
// আর Closure হলো সেই নিয়মের একটি "প্রয়োগ বা ফলাফল"।

// ১. লেক্সিক্যাল স্কোপ (Lexical Scope)
// "Lexical" শব্দের অর্থ হলো "Hierarchy" বা ক্রমবিন্যাস। জাভাস্ক্রিপ্টে লেক্সিক্যাল স্কোপের নিয়ম হলো—একটি ফাংশন যেখানে লেখা হয়েছে (Defined), 
// সে তার আশেপাশের (Parent) পরিবেশের ভেরিয়েবলগুলো দেখতে পাবে।
// কোডটি রান করার আগেই জাভাস্ক্রিপ্ট ইঞ্জিন দেখে নেয় কোন ফাংশন কার ভেতরে আছে এবং কে কার ডেটা এক্সেস করতে পারবে।

let boss = 'Damien';

function outer() {
    // এই ফাংশনটি boss-variable কে চেনে কারণ boss তার বাইরে (Global) আছে।
    function inner() {
        console.log(boss); // This is Lexical Scoping
    }
    inner();
}
outer();

// inner ফাংশনটি boss কে খুঁজে পাচ্ছে because লেক্সিক্যাল নিয়ম অনুযায়ী সে তার উপরের লেভেলের ডেটা এক্সেস করতে পারে।


// ২. ক্লোজার (Closure)
// ক্লোজার হলো লেক্সিক্যাল স্কোপের একটি বিশেষ ক্ষমতা। যখন কোনো ফাংশনকে তার লেক্সিক্যাল স্কোপের বাইরে নিয়ে গিয়ে কল করা হয়, 
// তখন সে তার পুরনো পরিবেশের ডেটা সাথে করে নিয়ে যায়। এই "সাথে করে নিয়ে যাওয়া"র ঘটনাটিই হলো ক্লোজার।


// ৩. মূল পার্থক্য কোথায়?
// Lexical Scope - 
// এটি একটি নিয়ম যা নির্ধারণ করে কোন ভেরিয়েবল কোথায় পাওয়া যাবে।
// কোড লেখার সময় বা কম্পাইল টাইমে এটি ঠিক হয়ে যায়।
// প্যারেন্ট Function'র ডেটা এক্সেস করা যায়। 

// Closure - 
// এটি একটি ফাংশন যা তার লেক্সিক্যাল স্কোপকে মনে রাখে।
// যখন একটি ফাংশন অন্য ফাংশন থেকে রিটার্ন হয় বা বেঁচে থাকে, তখন এটি তৈরি হয়।
// Parent Function শেষ হয়ে গেলেও তার ডেটা ব্যবহার করা যায়। 

function grandParent() {
    let house = 'Dewan House' // Lexical Scoap

    return function parent() {
        console.log('Welcome to ' + house);
    };
}

const visitHouse = grandParent();

visitHouse();

// parent ফাংশনটি যখন house কে এক্সেস করতে পারছে, তখন সেটা তার Lexical Scope।
// কিন্তু যখন grandParent() শেষ হয়ে যাওয়ার পরেও visitHouse() কল করার সময় সে house কে খুঁজে পাচ্ছে, তখন সেটা একটা Closure।

// 🎯 Real Life Analogy
// লেক্সিক্যাল স্কোপ হলো আপনার বাবার সম্পত্তি পাওয়ার আইন, আর ক্লোজার হলো সেই সম্পত্তি আপনার পকেটে থাকা।
